#include "libft.h"

int		ft_listlen(t_list *l)
{
	t_list	*l2;
	int		cpt;

	l2 = l;
	cpt = 0;
	while (l2)
	{
		cpt++;
		l2 = l2->next;
	}
	return (cpt);
}
