#include "libft.h"

char	*ft_strnew(size_t size)
{
	size_t	i;
	char	*s;

	i = 0;
	if (!(s = (char *)malloc(sizeof(*s) * (size + 1))))
		return (0);
	while (i < size + 1)
	{
		s[i] = '\0';
		i++;
	}
	return (s);
}
